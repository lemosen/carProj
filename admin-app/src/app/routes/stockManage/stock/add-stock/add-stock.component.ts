
import { Component, OnInit, ViewEncapsulation } from '@angular/core';
import { Router } from '@angular/router';
import { NzMessageService, NzModalService } from 'ng-zorro-antd';
import { Stock } from '../../../models/original/stock.model';
import { StockService } from '../../../services/stock.service';
import { SUCCESS } from '../../../models/constants.model';

@Component({
    selector: 'add-stock',
    templateUrl: './add-stock.component.html',
    styleUrls: ['./add-stock.component.scss'],
    encapsulation: ViewEncapsulation.None
})
export class AddStockComponent implements OnInit {

    submitting = false;

    stock: Stock;

    constructor(private router:Router,private stockService: StockService, public msgSrv: NzMessageService,
        private modalService: NzModalService) {
    }

    ngOnInit() {
    }

    onTransmitFormValueSubscription(event) {
        if (event) {
            this.confirmSub(event)
        }
    }

    confirmSub(formValue){
        if (formValue) {
            this.submitting = true;
            const messageId = this.msgSrv.loading("正在提交...").messageId;
            this.stockService.add(formValue.obj).subscribe(response => {
                if (response.result == SUCCESS) {
                    this.msgSrv.success("操作成功");
                    this.router.navigate(['/dashboard/stock/list']);
                } else {
                    this.msgSrv.error('请求失败'+response.message);
                }
                this.msgSrv.remove(messageId);
                this.submitting = false;
            }, error => {
                this.msgSrv.error('请求错误'+error.message);
                this.msgSrv.remove(messageId);
                this.submitting = false;
            });
        }
    }

}
